    private void tbl_candMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_candMouseClicked
        DefaultTableModel model = (DefaultTableModel)tbl_cand.getModel();
        int selectedRowIndex = tbl_cand.getSelectedRow();
        
        candid = Integer.parseInt(model.getValueAt(selectedRowIndex, 0).toString());
        
        try {
            
            String query = ("SELECT * FROM candidates where id=?");
            pst = conn.prepareStatement(query);
            pst.setInt(1,candid);

            ResultSet rs = pst.executeQuery();

        while (rs.next()) {            
            txt_name.setText(rs.getString("name"));
            txt_nic.setText(rs.getString("nic"));
            txt_tp.setText(rs.getString("telephone"));
            txt_email.setText(rs.getString("email"));
            
            boolean eligible = rs.getBoolean("eligibility");
            
            if(eligible){
                rb_yes.setSelected(true);
            }else{
                rb_no.setSelected(true);
            }
            
                        
        }
        
         } catch (Exception e) {
             
        JOptionPane.showMessageDialog(null,"Failed to Connect to Database","Error Connection", JOptionPane.WARNING_MESSAGE);  
        
        }
        
    }//GEN-LAST:event_tbl_candMouseClicked

