    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
            
        if (!(candid==0)){
            int input = JOptionPane.showConfirmDialog(null, "Do you want to proceed?", "Select an Option...",
                                        JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
            
            if(input==0){
                try {
                    String sql="DELETE FROM candidates WHERE id=?";
                    pst = conn.prepareStatement(sql);
                    pst.setInt(1, candid);
                    pst.execute();
                    
                    try {
            
                    Statement st = conn.createStatement();
                    ResultSet rs=st.executeQuery("SELECT * FROM candidates");

                    tableModel.setRowCount(0);

                    while (rs.next()) {  

                        String id = rs.getString("id");
                        String name = rs.getString("name");
                        String nic = rs.getString("nic");


                        String[] data = { id, name, nic } ;

                        tableModel.addRow(data);             
                    }

                     } catch (Exception e) {

                    JOptionPane.showMessageDialog(null,"Failed to Connect to Database","Error Connection", JOptionPane.WARNING_MESSAGE);  

                    }
                    tbl_cand.setModel(tableModel);
                    
                    
                    
                } catch (SQLException ex) {
                    Logger.getLogger(ComManCand.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select record.");
        }
        
        
            
    }//GEN-LAST:event_btn_deleteActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        
        if(!(candid==0)){
            
            if(!("".equals(txt_name.getText()) || "".equals(txt_nic.getText()) || "".equals(txt_email.getText()) || "".equals(txt_tp.getText()))){
                
                if(!(txt_tp.getText().length() > 10)){
                    
                    if(ve.isValidEmailAddress(txt_email.getText())){
                        
                        try {
            
                                String sql="UPDATE candidates SET name=?,nic=?,email=?,telephone=?,eligibility=? WHERE id=?";
                                pst = conn.prepareStatement(sql);
                                pst.setString(1,txt_name.getText());
                                pst.setString(2,txt_nic.getText());
                                pst.setString(3,txt_email.getText());
                                pst.setString(4,txt_tp.getText());

                                boolean eligible;

                                if(rb_yes.isSelected()){
                                    eligible = true;
                                }else{
                                    eligible = false;
                                }

                                pst.setBoolean(5, eligible);
                                pst.setInt(6, candid);

                                pst.execute();

                                JOptionPane.showMessageDialog(null, "Successfully Updated!");

                                try {

                                Statement st = conn.createStatement();
                                ResultSet rs=st.executeQuery("SELECT * FROM candidates");

                                tableModel.setRowCount(0);

                                while (rs.next()) {  

                                    String id = rs.getString("id");
                                    String name = rs.getString("name");
                                    String nic = rs.getString("nic");


                                    String[] data = { id, name, nic } ;

                                    tableModel.addRow(data);             
                                }

                                 } catch (Exception e) {

                                JOptionPane.showMessageDialog(null,"Failed to Connect to Database","Error Connection", JOptionPane.WARNING_MESSAGE);  

                                }
                                tbl_cand.setModel(tableModel);

                                }
                                catch (SQLException e)
                                {
                                    e.printStackTrace();
                                }
                        
                    }else{
                        JOptionPane.showMessageDialog(null, "Invalid email address.","Error!", JOptionPane.ERROR_MESSAGE);
                    }
                        
                }else{
                    JOptionPane.showMessageDialog(null, "Invalid telephone number.","Error!", JOptionPane.ERROR_MESSAGE);
                }
                
            }else{
                JOptionPane.showMessageDialog(null, "Cannot leave fields empty.","Error!", JOptionPane.ERROR_MESSAGE);
            }
            
             
        }else{
            JOptionPane.showMessageDialog(null, "Please select a candidate first.","Error!", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btn_updateActionPerformed

