    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        // TODO add your handling code here:

        try {
            String query = ("SELECT * FROM users where email=?");
            pst = conn.prepareStatement(query);
            pst.setString(1,txt_email.getText());

            ResultSet rs = pst.executeQuery(); 

        if(!rs.next()) {
              JOptionPane.showMessageDialog(null, "Invalid Credentials!");  
        } else {
            
            String password = new String(txt_pw.getPassword());
            String pw = rs.getString("password");
            String salt = rs.getString("pwsalt");
            int role = rs.getInt("role");
            
            Boolean status = PassEnc.verifyUserPassword(password,pw,salt);
            
            if(status==true){
                
                if(role==0){
                    
                    ComMenu cm = new ComMenu();
                    cm.setVisible(true);
                    this.dispose();
                    
                }else{
                    
                    OffMenu om = new OffMenu();
                    om.setVisible(true);
                    this.dispose();
                    
                }
            } 
                
            else {
                    JOptionPane.showMessageDialog(null, "Invalid Credentials!");  
                 }        
        }

         } catch (Exception e) {
             
             System.err.println( "Error!" );
         }
    }//GEN-LAST:event_btn_loginActionPerformed

