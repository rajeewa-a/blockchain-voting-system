    private void btn_regvoterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_regvoterActionPerformed
            
        
        if(!("".equals(txt_name.getText()) || "".equals(txt_nic.getText()) || "".equals(txt_email.getText()) || "".equals(txt_tp.getText()))){
                
                if(ve.isValidEmailAddress(txt_email.getText())){
                    
                    if(!(txt_tp.getText().length() > 10)){
                        
                        Random rnd = new Random();
                        int pin = rnd.nextInt(999999);

                        java.sql.Date date = new java.sql.Date(datechooser.getDate().getTime());      

                        try {

                        String sql="INSERT INTO voters (name,nic,email,telephone,address,district,dob,pin) VALUES (?,?,?,?,?,?,?,?)";
                        pst = conn.prepareStatement(sql);
                        pst.setString(1,txt_name.getText());
                        pst.setString(2,txt_nic.getText());
                        pst.setString(3,txt_email.getText());
                        pst.setString(4,txt_tp.getText());
                        pst.setString(5,txt_add.getText());
                        pst.setString(6,cb_district.getSelectedItem().toString());
                        pst.setDate(7,date);
                        pst.setInt(8,pin);
                        pst.execute();

                        JOptionPane.showMessageDialog(null, "Success");
                        }
                        catch (SQLException e)
                        {
                            e.printStackTrace();
                        }
                        
                                                
                    }else{
                        JOptionPane.showMessageDialog(null, "Invalid telephone number.","Error!", JOptionPane.ERROR_MESSAGE);
                    }
                    
                }else{
                    JOptionPane.showMessageDialog(null, "Invalid email address.","Error!", JOptionPane.ERROR_MESSAGE);
                }
                
            }else{
                JOptionPane.showMessageDialog(null, "Cannot leave fields empty.","Error!", JOptionPane.ERROR_MESSAGE);
            }

    }//GEN-LAST:event_btn_regvoterActionPerformed

